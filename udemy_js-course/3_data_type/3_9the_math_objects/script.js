// Math object properties
console.log(Math);
// result -- Math propertyes

let x;
// get the squere ruth of a number
x = Math.sqrt(9);
console.log(x);
// result -- 3

// get the absolute number
x = Math.abs(5);
console.log(x);
// result -- 5

// round property
x = Math.round(4.6);
console.log(x);
// result -- 5

// round up property
x = Math.ceil(4.2);
console.log(x);
// result -- 5

// round down property
x = Math.floor(4.8);
console.log(x);
// result -- 4

//pow property
x = Math.pow(3, 6);
console.log(x);
// result -- 729

//min property -- returns the smalest numbers
x = Math.min(3, 4, 5);
console.log(x);
// result -- 3

//max property -- returns the bigest numbers
x = Math.max(3, 4, 5);
console.log(x);
// result -- 5

//random property -- gives a random decimal between 0 and 9
x = Math.random() * 10;
console.log(x);
// result -- random large number

//random property -- gives a random number between 0 and 10
x = Math.floor(Math.random() * 10 + 1);
console.log(x);
// result -- random number
