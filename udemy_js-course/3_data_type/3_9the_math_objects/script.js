// Math -- object properties
console.log(Math);

let x;

// get the squere ruth of a number
x = Math.sqrt(9);
console.log(x);

// get the absolute number
x = Math.abs(5);
console.log(x);

// round property
x = Math.round(4.6);
console.log(x);

// round up property
x = Math.ceil(4.2);
console.log(x);

// round down property
x = Math.floor(4.8);
console.log(x);

//pow property
x = Math.pow(3, 6);
console.log(x);

//min property -- returns the smalest numbers
x = Math.min(3, 4, 5);
console.log(x);

//max property -- returns the smalest numbers
x = Math.max(3, 4, 5);
console.log(x);

//random property -- gives a random decimal between 0 and 9
x = Math.random() * 10;
console.log(x);

//random property -- gives a random number between 0 and 10
x = Math.floor(Math.random() * 10 + 1);
console.log(x);
