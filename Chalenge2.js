const x = Math.floor(Math.random() * 100 + 1);
const y = Math.floor(Math.random() * 50 + 1);

// get the sum
const sum = x + y;
const summOutputt = `${x} + ${y} = ${sum}`;
console.log(summOutputt);

// get the difference
const diference = x - y;
const diferenceOutputt = `${x} - ${y} = ${diference}`;
console.log(diferenceOutputt);

// get the product
const product = x * y;
const productOutputt = `${x} * ${y} = ${product}`;
console.log(productOutputt);

// get the quotient
const quotient = x / y;
const quotientOutputt = `${x} / ${y} = ${quotient}`;
console.log(quotientOutputt);

// get the remainder
const rm = x % y;
const rmOutputt = `${x} % ${y} = ${rm}`;
console.log(rmOutputt);
